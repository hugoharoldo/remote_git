    public PessoaComoboxModel getPessoaComoboxModel() {

        List<People> l = new ArrayList<>();
        l.add(new People());
        l.addAll(PessoaControle.getPessoas());

        PessoaComoboxModel comoboxModel = new PessoaComoboxModel();
        comoboxModel.addPessoas(l);

        return comoboxModel;
    }

